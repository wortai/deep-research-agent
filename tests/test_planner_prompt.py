    for i, demo in enumerate(DEMO_QUERIES):
        print_separator("=", 80)
        print(
            f"TEST #{demo['id']}/10 | Domain: {demo['domain']} | Complexity: {demo['complexity']}"
        )
        print(f'User Query: "{demo["query"]}"')
        print_separator("=", 80)

        try:
            prompt = get_enhanced_plan_prompt(
                query=demo["query"],
                skill_instructions="",
                clarification_context=[],
            )

            print("\n📝 GENERATED PROMPT (first 500 chars):")
            print(prompt[:500])
            print("...")
            print(f"\n📏 Total prompt length: {len(prompt)} characters")

            # Check for key elements in prompt
            checks = [
                ("Hard constraints section", "HARD CONSTRAINTS" in prompt),
                ("Query count framework", "DECIDE HOW MANY" in prompt),
                ("Domain adaptation", "ADAPT TO THE TOPIC" in prompt),
                ("Independence rule", "independently searchable" in prompt.lower()),
                ("Progression rule", "progressive" in prompt.lower()),
                ("Word limit mentioned", "20–100" in prompt or "20-100" in prompt),
            ]

            print("\n✅ Prompt Structure Checks:")
            for name, passed in checks:
                status = "✅" if passed else "❌"
                print(f"  {status} {name}")

        except Exception as e:
            print(f"❌ Error generating prompt: {e}")

        print("\n")


