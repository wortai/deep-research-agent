def count_words(text: str) -> int:
    """Count words in a string."""
    return len(text.strip().split())


def analyze_plan(query_info: dict, plan: list) -> dict:
    """Analyze a generated plan against quality criteria."""
    results = {
        "query_id": query_info["id"],
        "domain": query_info["domain"],
        "complexity": query_info["complexity"],
        "expected_range": query_info["expected_range"],
        "user_query": query_info["query"],
        "num_queries": len(plan),
        "queries": [],
        "violations": [],
        "scores": {},
    }

    # Check count constraint (1-10)
    if len(plan) < 1:
        results["violations"].append("❌ FAIL: Zero queries generated")
    elif len(plan) > 10:
        results["violations"].append(f"❌ FAIL: {len(plan)} queries exceeds MAX of 10")
    else:
        results["violations"].append(f"✅ PASS: {len(plan)} queries (within 1-10)")

    # Check expected range match
    expected = query_info["expected_range"]
    if "-" in expected:
        low, high = map(int, expected.split("-"))
        if low <= len(plan) <= high:
            results["violations"].append(
                f"✅ PASS: Count {len(plan)} matches expected range {expected}"
            )
        else:
            results["violations"].append(
                f"⚠️  WARN: Count {len(plan)} outside expected range {expected}"
            )

    # Analyze each query
    word_counts = []
    for i, q in enumerate(plan):
        wc = count_words(q)
        word_counts.append(wc)
        q_result = {
            "num": i + 1,
            "word_count": wc,
            "text": q,
            "within_limit": 20 <= wc <= 100,
        }
        results["queries"].append(q_result)

        if wc < 20:
            results["violations"].append(
                f"❌ FAIL: Query {i + 1} too short ({wc} words, min 20)"
            )
        elif wc > 100:
            results["violations"].append(
                f"❌ FAIL: Query {i + 1} too long ({wc} words, max 100)"
            )
        else:
            results["violations"].append(
                f"✅ PASS: Query {i + 1} word count = {wc} (within 20-100)"
            )

    # Word count statistics
    if word_counts:
        results["scores"]["avg_word_count"] = round(
            sum(word_counts) / len(word_counts), 1
        )
        results["scores"]["min_word_count"] = min(word_counts)
        results["scores"]["max_word_count"] = max(word_counts)

    # Check for progressive structure (heuristic)
    if len(plan) >= 3:
        first_half_avg = sum(word_counts[: len(word_counts) // 2]) / (
            len(word_counts) // 2
        )
        second_half_avg = sum(word_counts[len(word_counts) // 2 :]) / max(
            1, len(word_counts) - len(word_counts) // 2
        )
        results["scores"]["progressive_structure"] = (
            "Likely progressive"
            if second_half_avg >= first_half_avg * 0.8
            else "May lack progression"
        )

    # Check independence (heuristic: word overlap between queries)
    overlap_count = 0
    for i in range(len(plan)):
        for j in range(i + 1, len(plan)):
            words_i = set(plan[i].lower().split())
            words_j = set(plan[j].lower().split())
            if words_i & words_j:
                overlap = len(words_i & words_j) / min(len(words_i), len(words_j))
                if overlap > 0.5:
                    overlap_count += 1
    results["scores"]["overlap_score"] = (
        f"{overlap_count} potential overlaps detected"
        if overlap_count > 0
        else "No significant overlaps"
    )

    return results


def print_separator(char="=", length=80):
    print(char * length)


def print_results(results: list):
    """Print formatted analysis results."""
    print_separator()
    print("PLANNER PROMPT TEST RESULTS")
    print(f"Timestamp: {datetime.datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print_separator()

    total_pass = 0
    total_fail = 0

    for r in results:
        print_separator("-", 80)
        print(
            f"TEST #{r['query_id']} | Domain: {r['domain']} | Complexity: {r['complexity']}"
        )
        print(f'User Query: "{r["user_query"]}"')
        print(
            f"Queries Generated: {r['num_queries']} | Expected: {r['expected_range']}"
        )
        print_separator("-", 80)

        for i, q in enumerate(r["queries"]):
            status = "✅" if q["within_limit"] else "❌"
            print(f"\n  Query {q['num']} [{status}] ({q['word_count']} words):")
            print(f"    {q['text'][:150]}{'...' if len(q['text']) > 150 else ''}")

        print(f"\n  --- Scores ---")
        for key, val in r["scores"].items():
            print(f"    {key}: {val}")

        print(f"\n  --- Constraint Checks ---")
        for v in r["violations"]:
            print(f"    {v}")
            if "FAIL" in v:
                total_fail += 1
            elif "PASS" in v:
                total_pass += 1

        print()

    # Summary
    print_separator("=", 80)
    print("SUMMARY")
    print_separator("=", 80)
    print(f"Total Checks: {total_pass + total_fail}")
    print(f"Passed: {total_pass} ✅")
    print(f"Failed: {total_fail} ❌")
    if total_pass + total_fail > 0:
        pass_rate = (total_pass / (total_pass + total_fail)) * 100
        print(f"Pass Rate: {pass_rate:.1f}%")
    print_separator("=", 80)


